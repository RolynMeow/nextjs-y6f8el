var calc = {
  add: function (a, b) {
    let x = a + b;
    return x;
  },
  subt: function (a, b) {
    let x = a - b;
    return x;
  },
  multi: function (a, b) {
    let x = a * b;
    return x;
  },
  div: function (a, b) {
    let x = a / b;
    return x;
  },
};

module.exports = calc;
